    private void jButtonInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInicioActionPerformed

        boolean ok = Escribir.validar(jTextField1.getText(), jPasswordField1.getText());
        if (ok) {
            JOptionPane.showMessageDialog(this, "USUARIO VALIDO", "WARNING", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "USUARIO NO VALIDO", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonInicioActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean ok = Escribir.validar(jTextField1.getText(), jPasswordField1.getText());

        if (ok) {
            JOptionPane.showMessageDialog(this, "USUARIO YA EXISTENTE", "EXISTENTE", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "USUARIO GUARDADO", "NUEVO", JOptionPane.INFORMATION_MESSAGE);
            Escribir.escribir(jTextField1.getText(), jPasswordField1.getText());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

